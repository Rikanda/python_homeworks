Ax = int(input('Укажите координату х точки А : '))
Ay = int(input('Укажите координату y точки А : '))
Bx = int(input('Укажите координату х точки B : '))
By = int(input('Укажите координату y точки B : '))

distantion = ((Bx - Ax)**2 + (By - Ay)**2)**0.5
print('Расстояние между точками А и В составляет', distantion)